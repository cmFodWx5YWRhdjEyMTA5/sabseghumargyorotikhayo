package com.sanshy.farmmanagement;

public class ThreeColumnsList {

    String First;
    String Second;
    String Thired;

    public ThreeColumnsList(String first, String second, String thired) {
        First = first;
        Second = second;
        Thired = thired;
    }

    public String getFirst() {
        return First;
    }

    public void setFirst(String first) {
        First = first;
    }

    public String getSecond() {
        return Second;
    }

    public void setSecond(String second) {
        Second = second;
    }

    public String getThired() {
        return Thired;
    }

    public void setThired(String thired) {
        Thired = thired;
    }
}
